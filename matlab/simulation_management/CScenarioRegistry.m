    function strSpec = ApplyLengthUnitScaling_(strSpec, charLengthUnits)
        if strcmpi(charLengthUnits, "km")
            dLengthScale = 1.0e-3;
        else
            dLengthScale = 1.0;
        end

        dGravScale = dLengthScale^3;
        strSpec.charLengthUnits = string(charLengthUnits);
        strSpec.dGravParam = strSpec.dGravParam_m3s2 * dGravScale;
        strSpec.dReferenceRadius = strSpec.dReferenceRadius_m * dLengthScale;
        strSpec.dShapeReferenceSize = strSpec.dShapeReferenceSize_m * dLengthScale;
        strSpec.dEllipsoidAxes = strSpec.dEllipsoidAxes_m * dLengthScale;

        if all(isfinite(strSpec.dEllipsoidAxes)) && all(strSpec.dEllipsoidAxes > 0.0)
            strSpec.dTargetShapeMatrix_OF = diag(1.0 ./ (strSpec.dEllipsoidAxes .^ 2));
            strSpec.dTargetShapeMatrix_OF_m = diag(1.0 ./ (strSpec.dEllipsoidAxes_m .^ 2));
        end
        strSpec.strSphericalHarmonics = CScenarioRegistry.ScaleSphericalHarmonicsSpec_(strSpec.strSphericalHarmonics, dLengthScale);
    end

    function strSH = ScaleSphericalHarmonicsSpec_(strSH, dLengthScale)
        dGravScale = dLengthScale^3;
        strSH.dGravParam = strSH.dGravParam_m3s2 * dGravScale;
        strSH.dBodyRadiusRef = strSH.dBodyRadiusRef_m * dLengthScale;
    end

    function dGravConst = GetGravConstForLengthUnit_(charLengthUnits)
        if strcmpi(charLengthUnits, "km")
            dGravConst = 6.67430e-20;
        else
            dGravConst = 6.67430e-11;
        end
    end

