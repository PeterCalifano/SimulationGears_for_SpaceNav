methods (Static, Access = private)

    function charCanonicalName = ResolveCanonicalName_(enumOrName)
        % Resolve and make canonical scenario name for robust lookup
        charInput = lower(regexprep(char(string(enumOrName)), '[^a-zA-Z0-9]', ''));
        switch charInput
            case {'apophis'}
                charCanonicalName = "Apophis";
            case {'apophiselongated', 'apophismodified'}
                charCanonicalName = "ApophisElongated";
            case {'itokawa'}
                charCanonicalName = "Itokawa";
            case {'itokawamodified', 'modifieditokawa'}
                charCanonicalName = "ItokawaModified";
            case {'bennu', 'bennuorex'}
                charCanonicalName = "Bennu";
            case {'didymos', 'didymoshera'}
                charCanonicalName = "Didymos";
            case {'eros'}
                charCanonicalName = "Eros";
            case {'moon'}
                charCanonicalName = "Moon";
            case {'mars'}
                charCanonicalName = "Mars";
            case {'ceres'}
                charCanonicalName = "Ceres";
            case {'earth'}
                charCanonicalName = "Earth";
            case {'fromshape', 'custom', 'arbitraryshape'}
                charCanonicalName = "FromShape";
            case {'notdefined', 'passthrough'}
                charCanonicalName = "NotDefined";
            otherwise
                error('CScenarioRegistry:UnsupportedScenario', 'Unsupported scenario "%s".', string(enumOrName));
        end
    end

