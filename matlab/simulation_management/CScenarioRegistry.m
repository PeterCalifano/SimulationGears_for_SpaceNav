            enumOrName (1,:) {mustBeA(enumOrName, ["string", "char", "EnumScenarioName"])}
            ui32RequestedDegree (1,1) uint32
            charLengthUnits (1,:) string {mustBeA(charLengthUnits, ["string", "char"]), ...
                mustBeMember(charLengthUnits, ["m", "km"])} = "m"
        end

        strSpec = CScenarioRegistry.GetScenarioSpec(enumOrName, charLengthUnits=charLengthUnits);
        strSHmeta = strSpec.strSphericalHarmonics;
        strSHgravityData = CScenarioRegistry.EmptySphericalHarmonicsGravityData_();

        if ~strSHmeta.bHasHardcodedCoefficients || ui32RequestedDegree > strSHmeta.ui32HardcodedMaxDegree
            return
        end

        ui32RequiredRows = CScenarioRegistry.CountCoeffRows_(ui32RequestedDegree);
        strSHgravityData.dCSlmCoeffCols = strSHmeta.dCSlmCoeffCols(1:double(ui32RequiredRows), :);
        strSHgravityData.ui32MaxDegree = ui32RequestedDegree;
        strSHgravityData.dGravParam = strSHmeta.dGravParam;
        strSHgravityData.dBodyRadiusRef = strSHmeta.dBodyRadiusRef;
        strSHgravityData.dDensity = NaN;
        strSHgravityData.dGravConst = CScenarioRegistry.GetGravConstForLengthUnit_(charLengthUnits);
        strSHgravityData.strFitStats = struct( ...
            'charSource', strSHmeta.charSource, ...
            'charSourceUrl', strSHmeta.charSourceUrl, ...
            'charNormalization', strSHmeta.charNormalization, ...
            'charFieldKind', strSHmeta.charFieldKind, ...
            'ui32SourceMaxDegree', strSHmeta.ui32SourceMaxDegree, ...
            'ui32HardcodedMaxDegree', strSHmeta.ui32HardcodedMaxDegree, ...
