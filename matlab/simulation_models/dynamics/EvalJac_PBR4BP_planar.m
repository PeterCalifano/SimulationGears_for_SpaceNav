arguments (Input)
    dState      (:,1) double {mustBeNumeric}
    dMassRatio  (1,1) double {mustBeNumeric, mustBePositive, mustBeLessThan(dMassRatio, 1)}
    dTime       (1,1) double {mustBeNumeric}
end
arguments (Output)
    dJacobian   (4,4) double
end
%% PROTOTYPE
